fn main() {
/*this program is to understand the character sizes, 
u is for unsigned variables which can only store positive values
while i is for signed values that can take both positive and negative values*/
    let result = 10; //132 characters by default
    let age:u32 = 20;
    let sum:i32 = 5-15;

    println!("Result value is {}", result);
    println!("Age is {}",age );
    println!("Sum is {}",sum );
}
